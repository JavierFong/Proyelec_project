    public void obtenerUsuarios() {
        stm = database.getStatement();
        DefaultComboBoxModel model = new DefaultComboBoxModel(); 
        jComboBox1.setModel(model);
        try {
            ResultSet rs = stm.executeQuery("SELECT * FROM usuarios");
            while (rs.next()){
                model.addElement(rs.getString(1));
            }
        } catch (Exception e) {System.out.println("Error al cargar Usuarios"); }
    }
    
    public boolean verificarPW(String name, String pw){
        boolean flag = false;
        stm = database.getStatement(); 
        String cod = ""; 
        try {
            ResultSet rs = stm.executeQuery("SELECT * FROM usuarios WHERE Nombre='"+name+"';");
            while (rs.next()){
                cod = rs.getString(2); 
                if (cod != pw) {
                    flag = true;
                } 
            }
        } catch (Exception e) {System.out.println("Error al verificar identidad");}
        return flag; 
    }
    
    
    
    
    
